void SMG_dis()
{
	P2=0x00;
	P2|=0x80;  
	P2&=0x7f;
	
	P0=~SMG[SMG_cun[p]];
	P2|=0x40;
	P2&=0xbf;
	
	P2=T_COM[p];
	P2|=0x80;
	P2&=0x7f;
	p++;
	if(p>=4)
	{
		p=0;
	}
}